#pragma once

/*****************************************************************************\
MACRO: SIZE16
\*****************************************************************************/
#ifndef SIZE16
#define SIZE16( x )         ((DWORD)( sizeof(x) / sizeof(WORD) ))
#endif

/*****************************************************************************\
MACRO: SIZE32
\*****************************************************************************/
#ifndef SIZE32
#define SIZE32( x )         ((DWORD)( sizeof(x) / sizeof(DWORD) ))
#endif

/*****************************************************************************\
MACRO: SIZE64
\*****************************************************************************/
#ifndef SIZE64
#define SIZE64( x )         ((QWORD)( sizeof(x) / sizeof(QWORD) ))
#endif

/*****************************************************************************\
MACRO: OP_LENGTH
\*****************************************************************************/
#ifndef OP_LENGTH
#define OP_LENGTH( x )      ((DWORD)(x) - 2 )
#endif
